(defn list-directory [dir]
  (-> dir io/file .list))

(defn rename-file! [dir source-name dest-name]
  (let [source-path (str dir "/" source-name)
        dest-path (str dir "/" dest-name)]
    (io/copy (io/file source-path) (io/file dest-path))
    (io/delete-file source-path)
    (println source-name "->" dest-name)))

(defn rename-file-if-need [dir file-name]
  (loop [ks (keys m)]
    (when-let [k (first ks)]
      (let [p (re-pattern (str "[(]" k "[])]"))]
        (if (re-find p file-name)
          (let [new-file-name (string/replace file-name p (str "(" (get m k) ")"))]
            (rename-file! dir file-name new-file-name))
          (recur (rest ks)))))))

(defn -main [dir]
  (mapv
    (partial rename-file-if-need dir) 
    (list-directory dir)))

